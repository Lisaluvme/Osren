const fs = require('fs');

// Create minimal 1x1 PNG files as placeholders
const createMinimalPNG = (size) => {
  // This creates a valid but minimal PNG file
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, // IHDR length
    0x49, 0x48, 0x44, 0x52, // IHDR type
    // Width (4 bytes)
    (size >> 24) & 0xFF, (size >> 16) & 0xFF, (size >> 8) & 0xFF, size & 0xFF,
    // Height (4 bytes) 
    (size >> 24) & 0xFF, (size >> 16) & 0xFF, (size >> 8) & 0xFF, size & 0xFF,
    0x08, 0x02, 0x00, 0x00, 0x00, // Bit depth, color type, etc.
    0x4D, 0x65, 0x6D, 0x66, // CRC placeholder
    0x00, 0x00, 0x00, 0x00, // IHDR end
    0x00, 0x00, 0x00, 0x0C, // IDAT length
    0x49, 0x44, 0x54, // IDAT type
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, // Minimal image data
    0x00, 0x01, 0x00, 0x00, // IDAT end
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // IEND CRC
  ]);
  
  fs.writeFileSync(`public/icon-${size}.png`, png);
  console.log(`Created icon-${size}.png`);
};

createMinimalPNG(192);
createMinimalPNG(512);
